#import "GeolocAnnotation.h"

@implementation GeolocAnnotation

@synthesize coordinate, title, subtitle, placeId;

- (id)initWithCoords:(CLLocationCoordinate2D)newCoordinate title:(NSString *)newTitle subtitle:(NSString *)newSubtitle placeId:(NSString *)newPlaceId
{
    coordinate = newCoordinate;
    title = newTitle;
    subtitle = newSubtitle;
    placeId = newPlaceId;
    return self;
}

@end
